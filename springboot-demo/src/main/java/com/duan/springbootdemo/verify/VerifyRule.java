public enum VerifyRule {

    /**
     * != null
     */
    NOT_NULL,

    /**
     * array or list not empty
     */
    NOT_EMPTY,

    /**
     * string not blank
     */
    NOT_BLANK,

    /**
     * 非负数
     */
    NON_NEGATIVE

}
